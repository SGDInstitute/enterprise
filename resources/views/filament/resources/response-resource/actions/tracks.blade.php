        <h2 class="mb-4 mt-0 text-xl">Art, Media &amp; Design</h2>
        <p>
            Queer and trans folks know a thing or two about entertainment, content creation, media, and design! Whether
            the goal is advocacy for a cause, mobilizing people around an injustice, tightening a community through art
            and music, or bringing laughter and joy to your followers, media and content creation is a strong tool for
            gender justice and sexual liberation.&nbsp;
        </p>
        <p>Content in this track should focus on:</p>
        <ul>
            <li>Supporting artistic and creative content creation by queer and trans communities&nbsp;</li>
            <li>
                Representation, gaps in visibility, and analyzing the quality of queer and trans content in film,
                television, music&nbsp; journalism and other mass media&nbsp;
            </li>
            <li>
                Using social media and creative platforms to build community, raise awareness and mobilize
                movements&nbsp;
            </li>
        </ul>
    </div>
    <div>
        <h2 class="mb-4 mt-0 text-xl">Change on College Campuses</h2>
        <p>
            College campuses are key areas of influence for our conference attendees. The history of higher education is
            rife with instances in which queer and trans people are underrepresented in programming, initiatives, and
            opportunities. College students, educators, and practitioners play an integral role in expanding
            possibilities for our communities on campuses and improving campus climate for marginalized people.&nbsp;
        </p>
        <p>Content in this track should focus on:</p>
        <ul>
            <li>Experiences with student organizations and on-campus programming, events and advocacy&nbsp;</li>
            <li>Ways to access support services and resources as a queer/trans college student&nbsp;</li>
            <li>Advocating for improved campus policies and creating an affirming campus climate&nbsp;</li>
        </ul>
    </div>
    <div>
        <h2 class="mb-4 mt-0 text-xl">Justice: Activism &amp; Protest</h2>
        <p>
            Justice and rights will never be handed over by oppressive systems. They must be demanded. College campuses,
            urban centers, and rural communities have all been sites for direct action, protests, and other forms of
            activism in recent years specifically around issues of racial justice, police brutality, student loan debt,
            and climate catastrophes. Queer and trans folks are highly represented among those on the front lines, in
            medic tents, and leading mutual aid projects.&nbsp;
        </p>
        <p>Content in this track should focus on:</p>
        <ul>
            <li>Current movement work, mutual aid projects, direct action and other activism tactics</li>
            <li>
                Analyzing movements and activism throughout history to extract lessons for us in today&rsquo;s
                context&nbsp;
            </li>
            <li>Policies and political actions that pose barriers to activism efforts&nbsp;</li>
        </ul>
    </div>
    <div>
        <h2 class="mb-4 mt-0 text-xl">Self &amp; Community Care</h2>
        <p>
            Experiences of burnout, disruptions to existing organization and education efforts, and constantly reacting
            to waves of injustice have weighed heavily on queer and trans communities. It is essential we dedicate time
            and attention to pleasure, joy, wellness, and healing in order to sustain ourselves, our communities and our
            movement.&nbsp;
        </p>
        <p>Content in this track should focus on:&nbsp;</p>
        <ul>
            <li>
                Personal and community care practices that support communities impacted by burnout, systemic violence,
                trauma and medical gatekeeping&nbsp;
            </li>
            <li>
                Sexual health and safer sex practices including consent and&nbsp; boundaries, body image, kink/BDSM
                culture, relationship structures, and pleasure/desire mapping&nbsp;&nbsp;
            </li>
            <li>
                Improving and understanding mental health outcomes for queer and trans communities, especially in the
                Midwest, Southern and Appalachian regions&nbsp;&nbsp;
            </li>
        </ul>
    </div>
    <div>
        <h2 class="mb-4 mt-0 text-xl">Small-Town Queerness</h2>
        <p>
            Communities outside of urban spaces are often disregarded by political campaigning and resource allocation,
            leaving marginalized communities to manifest their own models of advocacy, organizing, and activism. By
            focusing on the work being done in rural communities to bridge resource gaps, build extensive communities,
            and combat small town conservative ideologies, we expand our toolkit of possible tactics and strategies for
            social change.&nbsp;
        </p>
        <p>Content in this track should focus on:&nbsp;</p>
        <ul>
            <li>
                Queer- and trans-focused work within rural and/or small communities in the Midwest, South and/or
                Appalachia&nbsp;
            </li>
            <li>Organizing, mutual aid projects and power building in rural communities</li>
            <li>Unique experiences of LGBTQ+ people in and from rural and/or small communities</li>
        </ul>
    </div>
    <div>
        <h2 class="mb-4 mt-0 text-xl">Advisor Track</h2>
        <p>
            We are huge fans of the folks on college campuses who dedicate themselves to supporting queer and trans
            college students as advisors, student support service staff or by way of &ldquo;other duties as
            assigned.&rdquo; The work performed by these individuals is often thankless or beyond their job description
            but plays a huge part in creating affirming spaces and experiences for queer and trans college students. We
            want to recognize this work and hold space for these roles who often attend the conference with their
            student groups and deserve a meaningful professional and personal development experience at
            MBLGTACC.&nbsp;&nbsp;
        </p>
        <p>Content in this track should focus on:</p>
        <ul>
            <li>
                Tools to support advisors, higher education practitioners and other roles that support college students
            </li>
            <li>
                Aiding advisors and practitioners in navigating complex campus environments and overcoming workplace
                barriers, burnout and other hardships of their positions&nbsp;
            </li>
            <li>
                Showcasing innovative and groundbreaking scholarship, research, programming and other initiatives that
                contribute to a culture of higher education in which sexuality and gender diversity is celebrated&nbsp;
            </li>
        </ul>
